// area of a triangle

#include<iostream>
using namespace std ;

int main(){
    int base = 10 ;
    int height = 12 ;
    cout << "area of a triangle with base " << base << " cm and  height " << height << " cm" << endl ;
    int area = 0.5 * base * height ;
    cout << "area of the triangle = " << area << " cm sq" ; 
    return 0 ;
}